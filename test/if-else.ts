export function uncovered(x: number): number {
    if (x > 0) {
        return x;
    } else {
        return -x;
    }
}

export function ifCovered(x: number): number {
    if (x > 0) {
        return x;
    } else {
        return -x;
    }
}

export function elseCovered(x: number): number {
    if (x > 0) {
        return x;
    } else {
        return -x;
    }
}

export function fullyCovered(x: number): number {
    if (x > 0) {
        return x;
    } else {
        return -x;
    }
}
